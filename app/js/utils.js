// 工具方法
function toTwo(time){
  if(time<10){
    return '0'+time;
  }else{
    return ''+time;
  }
}

// 十六进制转字符串
function hexToString(str){
  // 移除头尾空白
  str = str.trim();
  var val="";
  var sval="";
  var arr = str.split('');
  // 循环去除空格
  for(var i = 0; i < arr.length; i++){
    if(arr[i]!=' '){
      sval += arr[i]
    }
  }
  sval = sval.replace(/0x/g,'');
  var lng = sval.length;
  if(lng%2==1){
    sval = sval.substring(0,lng-1)+'0'+sval.substring(lng-1,lng);
  }

  for(var j=0;j<sval.length;j+=2){
    val += String.fromCharCode(parseInt(sval.substring(j,j+2),16));
  }
  return val;
}
